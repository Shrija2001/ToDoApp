package com.Modal;


public class Note {

	
	String Note;
	String AddDate;
	String EditDate;
	public String getNote() {
		return Note;
	}
	public void setNote(String note) {
		Note = note;
	}
	public String getAddDate() {
		return AddDate;
	}
	public void setAddDate(String addDate) {
		AddDate = addDate;
	}
	public String getEditDate() {
		return EditDate;
	}
	public void setEditDate(String editDate) {
		EditDate = editDate;
	}
	
	
}
